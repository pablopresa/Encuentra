
package main.process_ecommerce.mercadoLibreObjects;

import java.util.HashMap;
import java.util.Map;
import org.apache.commons.lang.builder.ToStringBuilder;
import org.codehaus.jackson.annotate.JsonIgnoreProperties;
@JsonIgnoreProperties(ignoreUnknown=true)
public class Seller {

    private long id;
    private String nickname;
    private String email;
    private Phone_ phone;
    private Alternative_phone_ alternative_phone;
    private String first_name;
    private String last_name;
    private Map<String, Object> additionalProperties = new HashMap<String, Object>();

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public Seller withId(long id) {
        this.id = id;
        return this;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public Seller withNickname(String nickname) {
        this.nickname = nickname;
        return this;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Seller withEmail(String email) {
        this.email = email;
        return this;
    }

    public Phone_ getPhone() {
        return phone;
    }

    public void setPhone(Phone_ phone) {
        this.phone = phone;
    }

    public Seller withPhone(Phone_ phone) {
        this.phone = phone;
        return this;
    }

    public Alternative_phone_ getAlternative_phone() {
        return alternative_phone;
    }

    public void setAlternative_phone(Alternative_phone_ alternative_phone) {
        this.alternative_phone = alternative_phone;
    }

    public Seller withAlternative_phone(Alternative_phone_ alternative_phone) {
        this.alternative_phone = alternative_phone;
        return this;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public Seller withFirst_name(String first_name) {
        this.first_name = first_name;
        return this;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public Seller withLast_name(String last_name) {
        this.last_name = last_name;
        return this;
    }

    public Map<String, Object> getAdditionalProperties() {
        return this.additionalProperties;
    }

    public void setAdditionalProperty(String name, Object value) {
        this.additionalProperties.put(name, value);
    }

    public Seller withAdditionalProperty(String name, Object value) {
        this.additionalProperties.put(name, value);
        return this;
    }

    @Override
    public String toString() {
        return new ToStringBuilder(this).append("id", id).append("nickname", nickname).append("email", email).append("phone", phone).append("alternative_phone", alternative_phone).append("first_name", first_name).append("last_name", last_name).append("additionalProperties", additionalProperties).toString();
    }

}
