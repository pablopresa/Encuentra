package dataTypes;

public class DataPedidoArticuloEcommerceVerif 
{
	private String fecha,idPedido,ojo,estadoEcommerce;
	private int unidadesTotal,totalProcesado;
	
	
	
	
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getIdPedido() {
		return idPedido;
	}
	public void setIdPedido(String idPedido) {
		this.idPedido = idPedido;
	}
	public String getOjo() {
		return ojo;
	}
	public void setOjo(String ojo) {
		this.ojo = ojo;
	}
	public String getEstadoEcommerce() {
		return estadoEcommerce;
	}
	public void setEstadoEcommerce(String estadoEcommerce) {
		this.estadoEcommerce = estadoEcommerce;
	}
	public int getUnidadesTotal() {
		return unidadesTotal;
	}
	public void setUnidadesTotal(int unidadesTotal) {
		this.unidadesTotal = unidadesTotal;
	}
	public int getTotalProcesado() {
		return totalProcesado;
	}
	public void setTotalProcesado(int totalProcesado) {
		this.totalProcesado = totalProcesado;
	}
	
	public DataPedidoArticuloEcommerceVerif(String fecha, String idPedido, String ojo, String estadoEcommerce,
			int unidadesTotal, int totalProcesado) {
		super();
		this.fecha = fecha;
		this.idPedido = idPedido;
		this.ojo = ojo;
		this.estadoEcommerce = estadoEcommerce;
		this.unidadesTotal = unidadesTotal;
		this.totalProcesado = totalProcesado;
	}
	
	
	
	

}
