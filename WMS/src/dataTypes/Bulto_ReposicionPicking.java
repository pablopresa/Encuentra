package dataTypes;

public class Bulto_ReposicionPicking {
	
	private String idBulto;
	private int cantidadArticulos;
	
	public Bulto_ReposicionPicking(String idBulto, int cantidadArticulos) {
		super();
		this.idBulto = idBulto;
		this.cantidadArticulos = cantidadArticulos;
	}

	public String getIdBulto() {
		return idBulto;
	}

	public void setIdBulto(String idBulto) {
		this.idBulto = idBulto;
	}

	public int getCantidadArticulos() {
		return cantidadArticulos;
	}

	public void setCantidadArticulos(int cantidadArticulos) {
		this.cantidadArticulos = cantidadArticulos;
	}
	
	

}
