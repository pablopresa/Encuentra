package dataTypes;

public class DataPiePerdido 
{
	private String articulo;
	private String pie;
	private String sucAlta;
	private String sucActual;
	private boolean respuesta;
	private int idAuditoria;
	private String fechaRe;	
	
	
	
	
	
	
	
	public String getFechaRe() {
		return fechaRe;
	}
	public void setFechaRe(String fechaRe) {
		this.fechaRe = fechaRe;
	}
	public int getIdAuditoria() {
		return idAuditoria;
	}
	public void setIdAuditoria(int idAuditoria) {
		this.idAuditoria = idAuditoria;
	}
	public boolean isRespuesta() {
		return respuesta;
	}
	public void setRespuesta(boolean respuesta) {
		this.respuesta = respuesta;
	}
	public String getArticulo() {
		return articulo;
	}
	public void setArticulo(String articulo) {
		this.articulo = articulo;
	}
	public String getPie() {
		return pie;
	}
	public void setPie(String pie) {
		this.pie = pie;
	}
	public String getSucAlta() {
		return sucAlta;
	}
	public void setSucAlta(String sucAlta) {
		this.sucAlta = sucAlta;
	}
	public String getSucActual() {
		return sucActual;
	}
	public void setSucActual(String sucActual) {
		this.sucActual = sucActual;
	}
	public DataPiePerdido(String articulo, String pie, String sucAlta, String sucActual, boolean res) {
		
		this.respuesta = res;
		this.articulo = articulo;
		this.pie = pie;
		this.sucAlta = sucAlta;
		this.sucActual = sucActual;
	}
	public DataPiePerdido() {
		
	}
	
	public DataPiePerdido(String articulo, String pie, String sucAlta,
			String sucActual) {
		this.articulo = articulo;
		this.pie = pie;
		this.sucAlta = sucAlta;
		this.sucActual = sucActual;
	}
	
	public DataPiePerdido(String articulo, String pie, String sucAlta,
			int idAuditoria) {
		this.articulo = articulo;
		this.pie = pie;
		this.sucAlta = sucAlta;
		this.idAuditoria = idAuditoria;
		
	}
	
	
	
	
	
	
	

}
