package dataTypes;

public class ArticuloPedido {
	private int lineaOrden, idDist, razon;
	private String articulo, destino, origen, coment;
	private int cantidadPedida;
	private int cantidadEntregada;
	
	public int getLineaOrden() {
		return lineaOrden;
	}
	public void setLineaOrden(int lineaOrden) {
		this.lineaOrden = lineaOrden;
	}
	public String getArticulo() {
		return articulo;
	}
	public void setArticulo(String articulo) {
		this.articulo = articulo;
	}
	public int getCantidadPedida() {
		return cantidadPedida;
	}
	public void setCantidadPedida(int cantidadPedida) {
		this.cantidadPedida = cantidadPedida;
	}
	public int getCantidadEntregada() {
		return cantidadEntregada;
	}
	public void setCantidadEntregada(int cantidadEntregada) {
		this.cantidadEntregada = cantidadEntregada;
	}
	public String getDestino() {
		return destino;
	}
	public void setDestino(String destino) {
		this.destino = destino;
	}
	public int getIdDist() {
		return idDist;
	}
	public void setIdDist(int idDist) {
		this.idDist = idDist;
	}
	public String getOrigen() {
		return origen;
	}
	public void setOrigen(String origen) {
		this.origen = origen;
	}
	public int getRazon() {
		return razon;
	}
	public void setRazon(int razon) {
		this.razon = razon;
	}
	public String getComent() {
		return coment;
	}
	public void setComent(String coment) {
		this.coment = coment;
	}

	
}
