package dataTypes;

public class Cantidades {
	private int solicitdas;
	private int recibidas;
	private int afacturar;
	
	public Cantidades(int int1, int int2,int int3) {
		solicitdas=int1;
		recibidas=int2;
		afacturar=int3;
	}
	public int getSolicitdas() {
		return solicitdas;
	}
	public void setSolicitdas(int solicitdas) {
		this.solicitdas = solicitdas;
	}
	public int getRecibidas() {
		return recibidas;
	}
	public void setRecibidas(int recibidas) {
		this.recibidas = recibidas;
	}
	public int getAfacturar() {
		return afacturar;
	}
	public void setAfacturar(int afacturar) {
		this.afacturar = afacturar;
	}
	

}
