package eCommerce_jsonObjectsII;
public class Presentaciones
{
    private String codigo;

    private PrecioLista precioLista;

    private String sku;

    private int stock;

    private PrecioVenta precioVenta;

    private String nombre;

    public void setCodigo(String codigo){
        this.codigo = codigo;
    }
    public String getCodigo(){
        return this.codigo;
    }
    public void setPrecioLista(PrecioLista precioLista){
        this.precioLista = precioLista;
    }
    public PrecioLista getPrecioLista(){
        return this.precioLista;
    }
    public void setSku(String sku){
        this.sku = sku;
    }
    public String getSku(){
        return this.sku;
    }
    public void setStock(int stock){
        this.stock = stock;
    }
    public int getStock(){
        return this.stock;
    }
    public void setPrecioVenta(PrecioVenta precioVenta){
        this.precioVenta = precioVenta;
    }
    public PrecioVenta getPrecioVenta(){
        return this.precioVenta;
    }
    public void setNombre(String nombre){
        this.nombre = nombre;
    }
    public String getNombre(){
        return this.nombre;
    }
}