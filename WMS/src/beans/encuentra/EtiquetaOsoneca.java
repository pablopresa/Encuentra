package beans.encuentra;

public class EtiquetaOsoneca 
{
	private String precio,talla, descripcion, barras, articulo;

	public String getPrecio() {
		return precio;
	}

	public void setPrecio(String precio) {
		this.precio = precio;
	}

	public String getTalla() {
		return talla;
	}

	public void setTalla(String talla) {
		this.talla = talla;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getBarras() {
		return barras;
	}

	public void setBarras(String barras) {
		this.barras = barras;
	}

	public String getArticulo() {
		return articulo;
	}

	public void setArticulo(String articulo) {
		this.articulo = articulo;
	}

	public EtiquetaOsoneca(String precio, String talla, String descripcion,
			String barras, String articulo) {
		this.precio = precio;
		this.talla = talla;
		this.descripcion = descripcion;
		this.barras = barras;
		this.articulo = articulo;
	}

	public EtiquetaOsoneca() {
	}
	
	
	
	
	
	
}
