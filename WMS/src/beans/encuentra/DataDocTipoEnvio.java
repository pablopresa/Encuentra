package beans.encuentra;

import java.util.List;

public class DataDocTipoEnvio 
{
	private int idTipo, cantidad;
	private String decTipo;
	private List <DocumentoEnvio> documentos;
	public DataDocTipoEnvio() {
		
	}
	public int getIdTipo() {
		return idTipo;
	}
	public void setIdTipo(int idTipo) {
		this.idTipo = idTipo;
	}
	public int getCantidad() {
		return cantidad;
	}
	public void setCantidad(int cantidad) {
		this.cantidad = cantidad;
	}
	public String getDecTipo() {
		return decTipo;
	}
	public void setDecTipo(String decTipo) {
		this.decTipo = decTipo;
	}
	public List<DocumentoEnvio> getDocumentos() {
		return documentos;
	}
	public void setDocumentos(List<DocumentoEnvio> documentos) {
		this.documentos = documentos;
	}
	
	
	
	
	

}
