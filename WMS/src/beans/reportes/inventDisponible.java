package beans.reportes;

public class inventDisponible 
{
	private String nombre,marca,genero,categoria, clase, articulo;
	private int cantidad;
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getGenero() {
		return genero;
	}
	public void setGenero(String genero) {
		this.genero = genero;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}
	public String getClase() {
		return clase;
	}
	public void setClase(String clase) {
		this.clase = clase;
	}
	public String getArticulo() {
		return articulo;
	}
	public void setArticulo(String articulo) {
		this.articulo = articulo;
	}
	public int getCantidad() {
		return cantidad;
	}
	public void setCantidad(int cantidad) {
		this.cantidad = cantidad;
	}
	public inventDisponible(String nombre, String marca, String genero, String categoria, String clase,
			String articulo, int cantidad) {
		this.nombre = nombre;
		this.marca = marca;
		this.genero = genero;
		this.categoria = categoria;
		this.clase = clase;
		this.articulo = articulo;
		this.cantidad = cantidad;
	}

	
	
	

}
