package beans.reportes;

public class Picking {
	private String nroSolicitud, articulo, clase, seccion, marca, ubicacion, destino, estado,
	nroDocumento, nroCaja, fechaSolicitud, fechaDocumento, usuario;

	public Picking(String nroSolicitud, String articulo, String clase, String seccion, String marca, 
			String ubicacion, String destino, String estado, String nroDocumento, String nroCaja,
			String fechaSolicitud, String fechaDocumento, String usuario) {
		this.nroSolicitud = nroSolicitud;
		this.articulo = articulo;
		this.clase = clase;
		this.seccion = seccion;
		this.marca = marca;
		this.ubicacion = ubicacion;
		this.destino = destino;
		this.estado = estado;
		this.nroDocumento = nroDocumento;
		this.nroCaja = nroCaja;
		this.fechaSolicitud = fechaSolicitud;
		this.fechaDocumento = fechaDocumento;
		this.usuario = usuario;
	}

	public String getNroSolicitud() {
		return nroSolicitud;
	}

	public String getArticulo() {
		return articulo;
	}

	public String getClase() {
		return clase;
	}

	public String getSeccion() {
		return seccion;
	}

	public String getMarca() {
		return marca;
	}

	public String getUbicacion() {
		return ubicacion;
	}

	public String getDestino() {
		return destino;
	}

	public String getEstado() {
		return estado;
	}

	public String getNroDocumento() {
		return nroDocumento;
	}

	public String getNroCaja() {
		return nroCaja;
	}

	public String getFechaSolicitud() {
		return fechaSolicitud;
	}

	public String getFechaDocumento() {
		return fechaDocumento;
	}

	public String getUsuario() {
		return usuario;
	}
}
