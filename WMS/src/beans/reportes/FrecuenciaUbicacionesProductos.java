package beans.reportes;

public class FrecuenciaUbicacionesProductos {
	private String ojo,articulo,hits,porcentaje;

	public FrecuenciaUbicacionesProductos(String ojo, String articulo, String hits, String porcentaje) {
		this.ojo = ojo;
		this.articulo = articulo;
		this.hits = hits;
		this.porcentaje = porcentaje;
	}

	public String getOjo() {
		return ojo;
	}

	public String getArticulo() {
		return articulo;
	}

	public String getHits() {
		return hits;
	}

	public String getPorcentaje() {
		return porcentaje;
	}
	
}
