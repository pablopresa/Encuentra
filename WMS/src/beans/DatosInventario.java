package beans;

public class DatosInventario {

	private int estanteria;
	private String descEstanteria;
	private String idOjo;
	private int estante;
	private int modulo;
	private int cantidad;
	private String fecha;
	
	
	
	public String getDescEstanteria() {
		return descEstanteria;
	}
	public void setDescEstanteria(String descEstanteria) {
		this.descEstanteria = descEstanteria;
	}
	public int getEstanteria() {
		return estanteria;
	}
	public void setEstanteria(int estanteria) {
		this.estanteria = estanteria;
	}
	public String getIdOjo() {
		return idOjo;
	}
	public void setIdOjo(String idOjo) {
		this.idOjo = idOjo;
	}
	public int getEstante() {
		return estante;
	}
	public void setEstante(int estante) {
		this.estante = estante;
	}
	public int getModulo() {
		return modulo;
	}
	public void setModulo(int modulo) {
		this.modulo = modulo;
	}
	public int getCantidad() {
		return cantidad;
	}
	public void setCantidad(int cantidad) {
		this.cantidad = cantidad;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	
	
	
	
}
