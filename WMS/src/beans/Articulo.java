package beans;

public class Articulo {

	private String articulo;
	private int proveedor;
	private String descripcion;
	private double costo;
	private int provOrigen,idMarca, idSeccion, idClase;
	

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String base() {

		String base = "";
		base = articulo.substring(0, 13);
		base = base + "";

		return base;
	}

	public String getArticulo() {
		return articulo;
	}

	public void setArticulo(String articulo) {
		this.articulo = articulo;
	}

	public int getProveedor() {
		return proveedor;
	}

	public void setProveedor(int proveedor) {
		this.proveedor = proveedor;
	}

	public Articulo(String articulo, int proveedor) {
		this.articulo = articulo;
		this.proveedor = proveedor;
	}

	
	
	
	public double getCosto() {
		return costo;
	}

	public void setCosto(double costo) {
		this.costo = costo;
	}

	public int getProvOrigen() {
		return provOrigen;
	}

	public void setProvOrigen(int provOrigen) {
		this.provOrigen = provOrigen;
	}

	public int getIdMarca() {
		return idMarca;
	}

	public void setIdMarca(int idMarca) {
		this.idMarca = idMarca;
	}

	public int getIdSeccion() {
		return idSeccion;
	}

	public void setIdSeccion(int idSeccion) {
		this.idSeccion = idSeccion;
	}

	public int getIdClase() {
		return idClase;
	}

	public void setIdClase(int idClase) {
		this.idClase = idClase;
	}

	public Articulo() {
	}

}
