package jsonObjects;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;

@JsonIgnoreProperties(ignoreUnknown = true)
public class trackingUES {
	private String uesId;
	private String estado_code;
	private String estado;
	private String subestado;
	private String fecha;
	private String pickup_lng;
	private String pickup;
	private String pickup_lat;
	
	public String getUesId() {
		return uesId;
	}
	public void setUesId(String uesId) {
		this.uesId = uesId;
	}
	public String getEstado_code() {
		return estado_code;
	}
	public void setEstado_code(String estado_code) {
		this.estado_code = estado_code;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getSubestado() {
		return subestado;
	}
	public void setSubestado(String subestado) {
		this.subestado = subestado;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getPickup_lng() {
		return pickup_lng;
	}
	public void setPickup_lng(String pickup_lng) {
		this.pickup_lng = pickup_lng;
	}
	public String getPickup() {
		return pickup;
	}
	public void setPickup(String pickup) {
		this.pickup = pickup;
	}
	public String getPickup_lat() {
		return pickup_lat;
	}
	public void setPickup_lat(String pickup_lat) {
		this.pickup_lat = pickup_lat;
	}
	
	public trackingUES() {}
	
}
