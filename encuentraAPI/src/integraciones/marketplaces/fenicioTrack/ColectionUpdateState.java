package integraciones.marketplaces.fenicioTrack;

import java.util.ArrayList;
import java.util.List;

import integraciones.marketplaces.objetos.UpdateStateResponse;

public class ColectionUpdateState {
	private boolean status;
	private String msg;
	private List<UpdateStateResponse> compras;
	
	public boolean isStatus() {
		return status;
	}
	public void setStatus(boolean status) {
		this.status = status;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public List<UpdateStateResponse> getCompras() {
		if(this.compras == null) {
			return new ArrayList<>();
		}
		else {
			return compras;
		}
		
	}
	public void setCompras(List<UpdateStateResponse> compras) {
		this.compras = compras;
	}
	
	
}
