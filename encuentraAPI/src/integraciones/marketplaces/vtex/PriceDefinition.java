package integraciones.marketplaces.vtex;
import java.util.ArrayList;
import java.util.List;
public class PriceDefinition
{
    private List<SellingPrices> sellingPrices;

    private int calculatedSellingPrice;

    private int total;

    public void setSellingPrices(List<SellingPrices> sellingPrices){
        this.sellingPrices = sellingPrices;
    }
    public List<SellingPrices> getSellingPrices(){
        return this.sellingPrices;
    }
    public void setCalculatedSellingPrice(int calculatedSellingPrice){
        this.calculatedSellingPrice = calculatedSellingPrice;
    }
    public int getCalculatedSellingPrice(){
        return this.calculatedSellingPrice;
    }
    public void setTotal(int total){
        this.total = total;
    }
    public int getTotal(){
        return this.total;
    }
}


