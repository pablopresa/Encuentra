package integraciones.marketplaces.MercadoLibre.entidades;

public class JSONArticuloPedidos
{
	private String codigoMoneda;

    private String codigoArticulo;

    private String subFamilia;

    private String precioSugeridoUnitario;

    private String cantidad;

    private String familia;

    public String getCodigoMoneda ()
    {
        return codigoMoneda;
    }

    public void setCodigoMoneda (String codigoMoneda)
    {
        this.codigoMoneda = codigoMoneda;
    }

    public String getCodigoArticulo ()
    {
        return codigoArticulo;
    }

    public void setCodigoArticulo (String codigoArticulo)
    {
        this.codigoArticulo = codigoArticulo;
    }

    public String getSubFamilia ()
    {
        return subFamilia;
    }

    public void setSubFamilia (String subFamilia)
    {
        this.subFamilia = subFamilia;
    }

    public String getPrecioSugeridoUnitario ()
    {
        return precioSugeridoUnitario;
    }

    public void setPrecioSugeridoUnitario (String precioSugeridoUnitario)
    {
        this.precioSugeridoUnitario = precioSugeridoUnitario;
    }

    public String getCantidad ()
    {
        return cantidad;
    }

    public JSONArticuloPedidos() {
		
	}

	public JSONArticuloPedidos(String codigoMoneda, String codigoArticulo,
			String subFamilia, String precioSugeridoUnitario, String cantidad,
			String familia) {
		super();
		this.codigoMoneda = codigoMoneda;
		this.codigoArticulo = codigoArticulo;
		this.subFamilia = subFamilia;
		this.precioSugeridoUnitario = precioSugeridoUnitario;
		this.cantidad = cantidad;
		this.familia = familia;
	}

	public void setCantidad (String cantidad)
    {
        this.cantidad = cantidad;
    }

    public String getFamilia ()
    {
        return familia;
    }

    public void setFamilia (String familia)
    {
        this.familia = familia;
    }

    @Override
    public String toString()
    {
        return "ClassPojo [codigoMoneda = "+codigoMoneda+", codigoArticulo = "+codigoArticulo+", subFamilia = "+subFamilia+", precioSugeridoUnitario = "+precioSugeridoUnitario+", cantidad = "+cantidad+", familia = "+familia+"]";
    }
}