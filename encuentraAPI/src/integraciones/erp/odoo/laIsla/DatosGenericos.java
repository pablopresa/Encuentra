package integraciones.erp.odoo.laIsla;

public class DatosGenericos {
	private int id;
	private String name, sexo;
	private String[] attribute_id;

	public DatosGenericos(String name, int id) {
		this.id = id;
		this.name = name;
	}
	
	public DatosGenericos(String[] attribute_id, String name, int id) {
		this.id = id;
		this.name = name;
		this.attribute_id = attribute_id;
	}
	
	public DatosGenericos(int id, String sexo) {
		this.id = id;
		this.sexo = sexo;
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public void setId(int id) {
		this.id = id;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String[] getAttribute_id() {
		return attribute_id;
	}

	public void setAttribute_id(String[] attribute_id) {
		this.attribute_id = attribute_id;
	}

	public String getSexo() {
		return sexo;
	}

	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
	
}
