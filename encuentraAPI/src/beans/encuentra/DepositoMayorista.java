package beans.encuentra;



public class DepositoMayorista 
{
	private String idDeposito,nombre,direccion,tipo,ciudad,departamento;
	
	

	public String getCiudad() {
		return ciudad;
	}

	public void setCiudad(String ciudad) {
		this.ciudad = ciudad;
	}

	public String getDepartamento() {
		return departamento;
	}

	public void setDepartamento(String departamento) {
		this.departamento = departamento;
	}

	public String getIdDeposito() {
		return idDeposito;
	}

	public void setIdDeposito(String idDeposito) {
		this.idDeposito = idDeposito;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getDireccion() {
		return direccion;
	}

	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public DepositoMayorista(String idDeposito, String nombre, String direccion, String tipo) {
		this.idDeposito = idDeposito;
		this.nombre = nombre;
		this.direccion = direccion;
		this.tipo = tipo;
	}
	
	
	

}
